class Command(BaseCommand):
    help = "Seed SEO-friendly content for dynamic pages (about, brotherhood, volunteers, running-club)."

    def add_arguments(self, parser):
        parser.add_argument(
            "--force",
            action="store_true",
            help="Replace existing sections on target pages with seed content.",
        )

    def _upsert_sections(self, page, sections, force=False):
        if force:
            page.sections.all().delete()
            for section in sections:
                PageSection.objects.create(
                    page=page,
                    title=section["title"],
                    text=section["text"],
                    order=section["order"],
                )
            return

        existing_by_order = {item.order: item for item in page.sections.all()}
        for section in sections:
            existing = existing_by_order.get(section["order"])
            if existing:
                existing.title = section["title"]
                existing.text = section["text"]
                existing.save(update_fields=["title", "text"])
            else:
                PageSection.objects.create(
                    page=page,
                    title=section["title"],
                    text=section["text"],
                    order=section["order"],
                )

    @transaction.atomic
    def handle(self, *args, **options):
        force = bool(options.get("force"))
        updated_count = 0

        for slug, payload in SEO_PAGES.items():
            page, _ = Page.objects.update_or_create(
                slug=slug,
                defaults={
                    "title": payload["title"],
                    "subtitle": payload["subtitle"],
                    "is_published": True,
                    "order": payload["order"],
                },
            )
            self._upsert_sections(page, payload["sections"], force=force)
            updated_count += 1

        mode_label = "force" if force else "safe"
        self.stdout.write(
            self.style.SUCCESS(
                f"Seed completed in {mode_label} mode. Pages processed: {updated_count}"
            )
        )
